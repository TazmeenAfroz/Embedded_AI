.INCLUDE"M328pDEF.INC"
.ORG 0
		LDI R16, HIGH (RAMEND)
		OUT SPH, R16
		LDI R16, LOW (RAMEND)
		OUT SPL, R16

		LDI R19, 0xFF
		OUT DDRD,R19
		
START:	
		LDI R20, 0x55
		OUT PORTD,R20
		RCALL DELAY1
		LDI R20, 0x0AA
		OUT PORTD,R20
		RCALL DELAY2

		JMP START
DELAY1:
		LDI R16, 200
AGAIN4:	LDI R17, 200
AGAIN5:	LDI R18, 10
AGAIN6:	DEC R18
		BRNE AGAIN6
		DEC R17
		BRNE AGAIN5
		DEC R16
		BRNE AGAIN4
		RET

DELAY2:
		LDI R16, 200
AGAIN3:	LDI R17, 200
AGAIN2:	LDI R18, 1
AGAIN1:	DEC R18
		BRNE AGAIN1
		DEC R17
		BRNE AGAIN2
		DEC R16
		BRNE AGAIN3
		RET
